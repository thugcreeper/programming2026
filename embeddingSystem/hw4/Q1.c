/*------------------------------------------------
 mS延時函數，含有輸入參數 unsigned char t，無返回值
 unsigned char 是定義無符號字符變量，其值的範圍是
 0~255 這裡使用晶振12M，精確延時請使用彙編
------------------------------------------------*/
void DelayMs(unsigned char t){
  while (t--){
    // 大致延時1mS
    DelayUs2x(245);
    DelayUs2x(245);
  }
}
